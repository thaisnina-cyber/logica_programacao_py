preco = 50.00
valor = 100.00

# comparativo de igualdade ==
print(preco == valor)
#false

# comparativo de diferença !=
print(preco != valor)
# true

# maior que / maior ou igual  > / >=
print(preco > valor)
# false
print(preco >= valor)
# false

# menor que / menor iqual que < /<=
print(preco < valor)
# true
print(preco <= valor)
#true


